create or replace function public.list_users_overview()
returns table (
  id uuid,
  name text,
  email text,
  phone text,
  account_status text,
  tasks_claimed integer,
  tasks_submitted integer,
  tasks_completed integer,
  balance double precision
)
language sql
security definer
set search_path = public
as $$
  select
    p.id,
    p.name,
    u.email,
    p.phone,
    p.account_status,
    count(a.id)::integer as tasks_claimed,
    coalesce(sum(case when a.state = 'submitted' then 1 else 0 end), 0)::integer as tasks_submitted,
    coalesce(sum(case when a.state = 'completed' then 1 else 0 end), 0)::integer as tasks_completed,
    coalesce(w.balance, 0) as balance
  from public.profiles p
  join auth.users u on u.id = p.id
  left join public.assignments a on a.user_id = p.id
  left join public.wallets w on w.user_id = p.id
  where public.is_admin()
    and p.role = 'user'
  group by p.id, p.name, u.email, p.phone, p.account_status, w.balance
  order by p.created_at desc;
$$;

create or replace function public.delete_user_account(p_user_id uuid)
returns boolean
language plpgsql
security definer
set search_path = public
as $$
begin
  if not public.is_admin() then
    raise exception 'Only admins can delete users.';
  end if;

  if not exists (
    select 1
    from public.profiles
    where id = p_user_id
      and role = 'user'
  ) then
    raise exception 'User not found.';
  end if;

  delete from auth.users
  where id = p_user_id;

  return true;
end;
$$;

create or replace function public.set_user_balance(
  p_user_id uuid,
  p_amount double precision
)
returns boolean
language plpgsql
security definer
set search_path = public
as $$
