create or replace function public.confirm_signup_payment(p_payment_id integer)
returns boolean
language plpgsql
security definer
set search_path = public
as $$
declare
  target_user_id uuid;
begin
  if not public.is_admin() then
    raise exception 'Only admins can confirm payments.';
  end if;

  select user_id
  into target_user_id
  from public.registration_payments
  where id = p_payment_id
    and status = 'pending';

  if target_user_id is null then
    raise exception 'Payment not found or already processed.';
  end if;

  update public.registration_payments
  set status = 'confirmed',
      confirmed_at = now(),
      admin_note = null
  where id = p_payment_id;

  update public.signup_requests
  set status = 'completed'
  where user_id = target_user_id;

  return true;
end;
$$;

create or replace function public.reject_signup_payment(
  p_payment_id integer,
  p_note text default null
)
returns boolean
language plpgsql
security definer
set search_path = public
as $$
declare
  target_user_id uuid;
begin
  if not public.is_admin() then
    raise exception 'Only admins can reject payments.';
  end if;

  select user_id
  into target_user_id
  from public.registration_payments
  where id = p_payment_id;

  if target_user_id is null then
    raise exception 'Payment not found.';
  end if;

  update public.registration_payments
  set status = 'rejected',
      admin_note = nullif(trim(coalesce(p_note, '')), '')
  where id = p_payment_id;

  update public.signup_requests
  set status = 'rejected'
  where user_id = target_user_id;

  return true;
end;
$$;

