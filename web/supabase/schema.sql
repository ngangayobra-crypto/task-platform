create or replace function public.review_submission(
  p_submission_id integer,
  p_review_status text,
  p_feedback text default null
)
returns boolean
language plpgsql
security definer
set search_path = public
as $$
declare
  target_assignment_id integer;
  target_user_id uuid;
  previous_status text;
  task_reward double precision;
begin
  if not public.is_admin() then
    raise exception 'Only admins can review submissions.';
  end if;

  if p_review_status not in ('approved', 'rejected') then
    raise exception 'Review status must be approved or rejected.';
  end if;

  select s.assignment_id, a.user_id, s.review_status, t.reward_amount
  into target_assignment_id, target_user_id, previous_status, task_reward
  from public.submissions s
  join public.assignments a on a.id = s.assignment_id
  join public.tasks t on t.id = a.task_id
  where s.id = p_submission_id
  for update;

  if target_assignment_id is null then
    raise exception 'Submission not found.';
  end if;

  if previous_status <> 'pending' then
    raise exception 'This submission has already been reviewed.';
  end if;

  update public.submissions
  set review_status = p_review_status,
      feedback = nullif(trim(coalesce(p_feedback, '')), '')
  where id = p_submission_id;

  if p_review_status = 'approved' then
    update public.assignments
    set state = 'completed'
    where id = target_assignment_id;

    insert into public.wallets (user_id, balance)
    values (target_user_id, 0)
    on conflict (user_id) do nothing;

    update public.wallets
    set balance = balance + coalesce(task_reward, 1.75)
    where user_id = target_user_id;
  else
    update public.assignments
    set state = 'assigned'
    where id = target_assignment_id;
  end if;

  return true;
end;
$$;

