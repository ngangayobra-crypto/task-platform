create or replace function public.submit_assignment(
  p_assignment_id integer,
  p_text text default null,
  p_file_path text default null
)
returns boolean
language plpgsql
security definer
set search_path = public
as $$
declare
  owner_id uuid;
  current_state text;
begin
  select user_id, state
  into owner_id, current_state
  from public.assignments
  where id = p_assignment_id;

  if owner_id is null then
    raise exception 'Assignment not found.';
  end if;

  if owner_id <> (select auth.uid()) then
    raise exception 'You can only submit your own assignments.';
  end if;

  if current_state not in ('assigned', 'submitted') then
    raise exception 'This assignment is not ready for submission.';
  end if;

  insert into public.submissions (assignment_id, text, file_path, submitted_at, review_status, feedback)
  values (p_assignment_id, nullif(trim(coalesce(p_text, '')), ''), p_file_path, now(), 'pending', null)
  on conflict (assignment_id) do update
    set text = excluded.text,
        file_path = coalesce(excluded.file_path, public.submissions.file_path),
        submitted_at = now(),
        review_status = 'pending',
        feedback = null;

  update public.assignments
  set state = 'submitted'
  where id = p_assignment_id;

  return true;
end;
$$;

