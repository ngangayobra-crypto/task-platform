create or replace function public.get_my_stats()
returns table (
  claimed integer,
  approved_to_start integer,
  completed integer,
  balance double precision
)
language sql
security definer
set search_path = public
as $$
  select
    coalesce(sum(case when a.state in ('claimed', 'assigned', 'submitted', 'reviewed', 'completed') then 1 else 0 end), 0)::integer as claimed,
    coalesce(sum(case when a.state = 'assigned' then 1 else 0 end), 0)::integer as approved_to_start,
    coalesce(sum(case when a.state = 'completed' then 1 else 0 end), 0)::integer as completed,
    coalesce(w.balance, 0) as balance
  from public.wallets w
  left join public.assignments a on a.user_id = w.user_id
  where w.user_id = (select auth.uid())
  group by w.balance;
$$;

create or replace function public.claim_task(p_task_id integer)
returns integer
language plpgsql
security definer
set search_path = public
as $$
declare
  next_assignment_id integer;
begin
  if not public.is_active_user() then
    raise exception 'Your account is not active yet.';
  end if;

  if not public.has_confirmed_payment() then
    raise exception 'Submit and confirm your M-Pesa payment first.';
  end if;

  if not exists (
    select 1
    from public.tasks
    where id = p_task_id
      and status = 'open'
  ) then
    raise exception 'Task not found or not open.';
  end if;

  if exists (
    select 1
    from public.assignments
    where task_id = p_task_id
      and user_id = (select auth.uid())
  ) then
    raise exception 'You already have this task in your list.';
  end if;

  insert into public.assignments (task_id, user_id, state)
  values (p_task_id, (select auth.uid()), 'claimed')
  returning id into next_assignment_id;

  return next_assignment_id;
end;
$$;

create or replace function public.request_withdrawal()
returns table (
  ok boolean,
  amount double precision
)
language plpgsql
security definer
set search_path = public
as $$
declare
  current_balance double precision;
begin
  if not public.is_active_user() then
    raise exception 'Your account is not active yet.';
  end if;

  if not public.has_confirmed_payment() then
    raise exception 'Submit and confirm your M-Pesa payment first.';
  end if;

  select balance
  into current_balance
  from public.wallets
  where user_id = (select auth.uid())
  for update;

  current_balance := coalesce(current_balance, 0);

  if current_balance < 500 then
    raise exception 'Withdraw unlocks at $500.';
  end if;

  insert into public.withdrawals (user_id, amount)
  values ((select auth.uid()), current_balance);

  update public.wallets
  set balance = 0
  where user_id = (select auth.uid());

  return query select true, current_balance;
end;
$$;

