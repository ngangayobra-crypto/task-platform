create or replace function public.assign_task(
  p_task_id integer,
  p_user_id uuid
)
returns integer
language plpgsql
security definer
set search_path = public
as $$
declare
  next_assignment_id integer;
begin
  if not public.is_admin() then
    raise exception 'Only admins can assign tasks.';
  end if;

  if not exists (
    select 1
    from public.tasks
    where id = p_task_id
      and status = 'open'
  ) then
    raise exception 'Task not found or not open.';
  end if;

  if exists (
    select 1
    from public.assignments
    where task_id = p_task_id
      and user_id = p_user_id
  ) then
    raise exception 'That user already has this task.';
  end if;

  if not exists (
    select 1
    from public.profiles
    where id = p_user_id
      and role = 'user'
      and account_status in ('active', 'pending')
      and public.has_confirmed_payment(p_user_id)
  ) then
    raise exception 'You can only assign tasks to users with confirmed payment.';
  end if;

  insert into public.assignments (task_id, user_id, state)
  values (p_task_id, p_user_id, 'assigned')
  returning id into next_assignment_id;

  return next_assignment_id;
end;
$$;

